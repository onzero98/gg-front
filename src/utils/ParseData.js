export function killDeathAssist(kill, death, assist){
  return String(kill) + "/" + String(death) + "/" + String(assist);
}

export function grade(kill, death, assist){
  if(death !== 0)
      return ((kill + assist) / death).toFixed(2);
  else if(death === 0){
      var res = kill + assist;
      if(res === 0){
          return 0;
      }
      else{
          return res;
      }
  }
}

